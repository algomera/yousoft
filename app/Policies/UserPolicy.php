    /**
     * Determine whether the user can create models.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
	    if ($user->can('create_users')) {
		    return true;
	    }
	    return Response::deny('Non sei autorizzato a creare utenti');
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function update(User $user, User $model)
    {
	    if ($user->can('update_users')) {
		    // Se Practice appartiene a User
		    if ($model->created_by->id == $user->id) {
			    return true;
		    }
	    }
	    return Response::deny('Non sei autorizzato ad aggiornare l\'utente');
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function delete(User $user, User $model)
    {
	    if ($user->can('delete_users')) {
		    // Se Practice appartiene a User
		    if ($model->created_by->id == $user->id) {
			    return true;
		    }
	    }
	    return Response::deny('Non sei autorizzato ad eliminare l\'utente');
    }

